  member((U, V), [
    (0, 1), (1, 2), (2, 3),
    (6, 7), (8, 9),
    (10, 11), (12, 13),
    (15, 16), (18, 19),
    (21, 22), (22, 23), (23, 24),
    (5, 10), (10, 15), (15, 20),
    (1, 6), (6, 11), (16, 21),
    (12, 17), (17, 22),
    (8, 13), (13, 18),
    (4, 9), (14, 19), (19, 24)
    ]
  ).
adj(U, V) :- adj0(U, V).
adj(U, V) :- adj0(V, U).

